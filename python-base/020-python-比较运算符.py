# 比较运算符
# 该类型运算符，返回值都为布尔类型

# 等等与（==）：判断两边的变量的值是否一样，是（true）
a = 1
b = 1
print('a与b是否相等：' + str(a == b))

# 不等于（！+）：判断两边的变量是否不一样，是（true）
c = 1
d = 2
print('c与d是否不相等：' + str(c != d))
# 拓展：<>是python2版本中的不等于，python3中被遗弃

