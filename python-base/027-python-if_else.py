# if else（否则）
"""
格式：
if 判断条件：
    True时，执行的代码
else：
    False时，执行的代码
"""
age = input('请输入您的年龄：')
age = int(age)
if age >= 18:
    print('恭喜您！已成年😎😎😎😎😎😎😎')
else:
    print('sorry,您未成年！！😒😒😒😒😒')














