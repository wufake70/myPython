# list 列表
'''
应用场景：当获取到很多个数据的时候，那么我们可以将它们存储到列表中，
然后直接使用列表访问
'''
name_list = ['周杰伦', '科比', '波多野结衣']
print(name_list)



# tuple 元组
age_tuple = (18, 99, 90, 50)
print(age_tuple)




