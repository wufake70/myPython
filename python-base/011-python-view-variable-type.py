

# 通过type（）方法查看数据的类型


# int
a = 22
print(a)
# <class 'int'>
print(type(a))

# float
a = 1.33
# <class 'float'>
print(type(a))

# boolean
b = False
c = True
# <class 'bool'>
print(type(b))

# string
d = '你好，世界！！'
# <class 'str'>
print(type(d))


# list
e = [1, 2, 3, 4, 5, 6]
# <class 'list'>
print(type(e))


# tuple
f = (1, 2, 3, 4, 5)
# <class 'tuple'>
print(type(f))


# dictionary
g = {'name': '波多野结衣', 'neme': '孙悟空'}
# <class 'dict'>
print(type(g))


















