"""
复合赋值运算符


"""

a = 2
# 加等于（+=）
# 结果为11
a += 9

# 减等于（-=）
# 结果为9
a -= 2

# 乘等于（*=）
# 结果为90
a *= 10

# 除等于（/=）
# 结果为10
a /= 9

# 余等于 （%=）
# 结果为1
a %= 9

# 整除等于（//=）取整
# 结果为1
a //= 2/3

# 幂等于（**=）
# 结果为1
a **= 3

print(a)








































