# 算数运算符

a = 2
b = 3
print('a+b=' + str(a + b))
print('a-b=' + str(a - b))
print('a*b=' + str(a * b))
print('a/b=' + str(a / b))

# 整除（//）：舍去浮点数，取整数部分
# 结果为零
print('整除运算 a // b=' + str(a // b))

# 趋于（%）：舍去整数，取余数部分
# 结果为 2
print('取余运算 a % b=' + str(a % b))


# 指数（幂）运算（**）
# 结果为8
print('指数运算 a ** b=' + str(a ** b))


